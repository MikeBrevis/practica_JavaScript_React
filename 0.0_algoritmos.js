// 1. Hacer click en el boton o link necesario para reestablecer la contraseña

// 2. Abrir el menu desplegable de la contraseña

// 3. Solicitar el metodo que desea aplicar, sea correo, mensaje de texto o llamada.

// 4. Si se solicito el correo, enviar un correo con un enlace para reestablecer la contraseña

// 5. Si se solicito el mensaje de texto, enviar un mensaje de texto con un enlace para reestablecer la contraseña

// 6. Si se solicito la llamada, hacer una llamada y solicitar el numero de telefono

// 7. Ingresar los datos correspondientes segun el metodo seleccionado

// 8. Pedir nueva contraseña y verificar que sea correcta

// 9. Si los datos son correctos, proceder a reestablecer la contraseña

// 10. Si los datos son incorrectos, solicitar que se ingresen los datos nuevamente.

// 11. Fin del programa.

